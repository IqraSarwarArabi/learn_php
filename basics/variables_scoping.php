$_REQUEST: Combines $_GET, $_POST, and $_COOKIE data.
$_POST: Contains data sent to the script via HTTP POST method.
$_GET: Contains data sent to the script via URL query parameters (GET method).
$_FILES: Contains information about uploaded files via HTTP POST.
$_ENV: Provides access to environment variables.
$_COOKIE: Contains data sent by the client's browser as cookies.
$_SESSION: Provides access to session variables.
$php_errormsg: Contains the last error message generated by PHP.
$HTTP_RAW_POST_DATA: Contains raw POST data.
$http_response_header: Contains the headers of the last HTTP response.
