<?php
// Global Scope
$globalVar = 10;

function globalExample() {
    echo "Global variable inside function: " . $GLOBALS['globalVar'] . "<br>";
}

globalExample(); // Output: Global variable inside function: 10

// Function Scope
function functionScope() {
    $localVar = 5; // Only accessible within this function
    echo "Local variable inside function: " . $localVar . "<br>";
}

functionScope(); // Output: Local variable inside function: 5

// Block Scope (PHP 7.0+)
if (true) {
    $blockVar = "Hello"; // Only accessible within this block
    echo "Block variable: " . $blockVar . "<br>";
}

// Class Scope
class MyClass {
    public $classVar = 42; // Accessible within class

    public function printVar() {
        echo "Class variable: " . $this->classVar . "<br>";
    }
}

$obj = new MyClass();
$obj->printVar(); // Output: Class variable: 42<br>

// Static Variable Scope
function counter() {
    static $count = 0; // Persists between function calls
    $count++;
    echo "Counter: " . $count . "<br>";
}

counter(); // Output: Counter: 1
counter(); // Output: Counter: 2

// Super Global Scope
$name = $_GET['name']; // Accessing superglobal variable
echo "Value from query parameter 'name': " . $name;
?>

